// Create a function that prints out your contact details and call it to
// produce the output.
function printContactDetails(name: string, email: string, phone: string): void {
    console.log(`Name: ${name}`);
    console.log(`Email: ${email}`);
    console.log(`Phone: ${phone}`);
}

